/**
 * Enhanced semantic clustering algorithm for dream analysis
 * @param {Array} words - Array of word objects with word, frequency, and stem
 * @returns {Object} - Object containing clusters and enhanced similarities
 */
export function getEnhancedSemanticClusters(words) {
  // Calculate multiple types of similarities
  const similarities = [];
  const clusters = [];

  // 1. Stem-based similarity (same root word)
  const stemGroups = {};
  words.forEach((wordObj, index) => {
    const stem = wordObj.stem;
    if (!stemGroups[stem]) {
      stemGroups[stem] = [];
    }
    stemGroups[stem].push(index);
  });

  // Add stem-based connections
  Object.values(stemGroups).forEach((group) => {
    if (group.length > 1) {
      for (let i = 0; i < group.length; i++) {
        for (let j = i + 1; j < group.length; j++) {
          similarities.push({
            source: group[i],
            target: group[j],
            similarity: 1,
            type: "stem",
            sourceWord: words[group[i]].word,
            targetWord: words[group[j]].word,
          });
        }
      }
    }
  });

  // 2. Semantic category similarity
  const wordCategories = {};
  words.forEach((wordObj, index) => {
    const word = wordObj.word.toLowerCase();
    Object.entries(dreamSemanticCategories).forEach(
      ([category, categoryWords]) => {
        if (
          categoryWords.includes(word) ||
          categoryWords.some(
            (catWord) => word.includes(catWord) || catWord.includes(word)
          )
        ) {
          if (!wordCategories[category]) {
            wordCategories[category] = [];
          }
          wordCategories[category].push(index);
        }
      }
    );
  });

  // Add semantic category connections
  Object.values(wordCategories).forEach((group) => {
    if (group.length > 1) {
      for (let i = 0; i < group.length; i++) {
        for (let j = i + 1; j < group.length; j++) {
          similarities.push({
            source: group[i],
            target: group[j],
            similarity: 0.9,
            type: "semantic",
            sourceWord: words[group[i]].word,
            targetWord: words[group[j]].word,
          });
        }
      }
    }
  });

  // 3. Levenshtein distance similarity (for typos and variations)
  for (let i = 0; i < words.length; i++) {
    for (let j = i + 1; j < words.length; j++) {
      const word1 = words[i].word;
      const word2 = words[j].word;

      if (word1.length > 3 && word2.length > 3) {
        const distance = natural.LevenshteinDistance(word1, word2);
        const maxLength = Math.max(word1.length, word2.length);
        const similarity = 1 - distance / maxLength;

        if (similarity > 0.9) {
          similarities.push({
            source: i,
            target: j,
            similarity: similarity,
            type: "levenshtein",
            sourceWord: word1,
            targetWord: word2,
          });
        }
      }
    }
  }

  // 4. Cosine similarity for remaining words
  for (let i = 0; i < words.length; i++) {
    for (let j = i + 1; j < words.length; j++) {
      const word1 = words[i].word;
      const word2 = words[j].word;

      // Skip if already connected by other methods
      const alreadyConnected = similarities.some(
        (sim) =>
          (sim.source === i && sim.target === j) ||
          (sim.source === j && sim.target === i)
      );

      if (!alreadyConnected) {
        const similarity = cosineSimilarityScore(word1, word2);
        if (similarity > 0.9) {
          similarities.push({
            source: i,
            target: j,
            similarity: similarity,
            type: "cosine",
            sourceWord: word1,
            targetWord: word2,
          });
        }
      }
    }
  }

  // 5. Find connected components (clusters)
  const visited = new Set();
  const clusterIndex = {};
  let clusterCount = 0;

  const dfs = (nodeIndex, currentCluster) => {
    if (visited.has(nodeIndex)) return;

    visited.add(nodeIndex);
    clusterIndex[nodeIndex] = currentCluster;

    // Find all connected nodes
    similarities.forEach((sim) => {
      if (sim.source === nodeIndex && !visited.has(sim.target)) {
        dfs(sim.target, currentCluster);
      } else if (sim.target === nodeIndex && !visited.has(sim.source)) {
        dfs(sim.source, currentCluster);
      }
    });
  };

  // Create clusters
  for (let i = 0; i < words.length; i++) {
    if (!visited.has(i)) {
      dfs(i, clusterCount);
      clusterCount++;
    }
  }

  // Group clusters
  const clusterGroups = {};
  Object.entries(clusterIndex).forEach(([nodeIndex, clusterId]) => {
    if (!clusterGroups[clusterId]) {
      clusterGroups[clusterId] = [];
    }
    clusterGroups[clusterId].push(parseInt(nodeIndex));
  });

  // Convert to array format
  Object.entries(clusterGroups).forEach(([clusterId, nodes]) => {
    if (nodes.length > 1) {
      clusters.push({
        id: clusterId,
        nodes: nodes,
        size: nodes.length,
        category: detectClusterCategory(nodes, words),
        words: nodes.map((nodeIndex) => words[nodeIndex].word),
      });
    }
  });

  return {
    similarities: similarities,
    clusters: clusters,
  };
}

/**
 * Detect the main category of a cluster based on its words
 */
function detectClusterCategory(nodeIndices, words) {
  const clusterWords = nodeIndices.map((index) =>
    words[index].word.toLowerCase()
  );

  let bestCategory = "general";
  let bestCount = 0;

  Object.entries(dreamSemanticCategories).forEach(
    ([category, categoryWords]) => {
      const count = clusterWords.filter(
        (word) =>
          categoryWords.includes(word) ||
          categoryWords.some(
            (catWord) => word.includes(catWord) || catWord.includes(word)
          )
      ).length;

      if (count > bestCount) {
        bestCount = count;
        bestCategory = category;
      }
    }
  );

  return bestCategory;
}

/**
 * Simple cosine similarity for fallback
 */
function cosineSimilarityScore(word1, word2) {
  const vec1 = word1.split("").reduce((acc, char) => {
    acc[char] = (acc[char] || 0) + 1;
    return acc;
  }, {});

  const vec2 = word2.split("").reduce((acc, char) => {
    acc[char] = (acc[char] || 0) + 1;
    return acc;
  }, {});

  const keys = new Set([...Object.keys(vec1), ...Object.keys(vec2)]);
  let dotProduct = 0;
  let norm1 = 0;
  let norm2 = 0;

  keys.forEach((key) => {
    const val1 = vec1[key] || 0;
    const val2 = vec2[key] || 0;
    dotProduct += val1 * val2;
    norm1 += val1 * val1;
    norm2 += val2 * val2;
  });

  return dotProduct / (Math.sqrt(norm1) * Math.sqrt(norm2));
}
